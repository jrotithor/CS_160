operation = input("Enter an operation:");
num1 = float(input("Enter your first number:"));
num2 = float (input("Enter your second number:"));
if (operation == "+"):
    print(num1+num2)
elif (operation == "-"):
    print(num1-num2)
elif (operation == "*"):
    print(num1*num2)
elif (operation == "/"):
    print(num1 / num2)
elif (operation == "**"):
    print(num1 ** num2)
elif (operation == "%"):
    print(num1%num2);

